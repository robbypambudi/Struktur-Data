bool getPath(AVLNode *root, vector<int> &arr, int x)
{
    if (root == NULL)
        return false;

    arr.push_back(root->data);

    // if it is the required node
    // return true
    if (root->data == x)
        return true;

    if (getPath(root->left, arr, x) || getPath(root->right, arr, x))
        return true;

    arr.pop_back();
    return false;
}
void printPathBetweenNodes(AVLNode *root, int n1, int n2)
{
    vector<int> path1, path2;
    getPath(root, path1, n1);
    getPath(root, path2, n2);

    int intersection = -1;

    // Get intersection point
    int i = 0, j = 0;
    while (i != path1.size() || j != path2.size())
    {

        // Keep moving forward until no intersection
        // is found
        if (i == j && path1[i] == path2[j])
        {
            i++;
            j++;
        }
        else
        {
            intersection = j - 1;
            break;
        }
    }
    vector<int> path;
    for (int i = path1.size() - 1; i > intersection; i--)
        path.push_back(path1[i]);

    for (int i = intersection; i < path2.size(); i++)
        path.push_back(path2[i]);

    int result = 0;
    for (int i = 1; i < path.size(); i++)
        result += abs(path[i - 1] - path[i]);

    cout << result << endl;
}

